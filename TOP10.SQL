
SET SERVEROUTPUT ON
CREATE OR REPLACE PROCEDURE GET_TOP10_EMPLOYEES 
IS
CURSOR C1 
IS
SELECT* FROM(SELECT EMPLOYEE_ID,FIRST_NAME,
TRUNC(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)AS EXPERIENCE
FROM EMPLOYEES
ORDER BY EXPERIENCE DESC)
WHERE ROWNUM<=10;
EMP_REC C1%ROWTYPE;
BEGIN
OPEN C1;
LOOP
FETCH C1 INTO EMP_REC;
EXIT WHEN C1%NOTFOUND;
DBMS_OUTPUT.PUT_LINE(EMP_REC.EMPLOYEE_ID||'  '||EMP_REC.FIRST_NAME|| ' '||EMP_REC.EXPERIENCE||'YEARS');
END LOOP;
CLOSE C1;
END;
