DECLARE 
V_EMP EMPLOYEES%ROWTYPE;
CURSOR C1
IS
SELECT* FROM EMPLOYEES WHERE DEPARTMENT_ID IN(30,90);
BEGIN
OPEN C1;
LOOP
FETCH C1 INTO V_EMP;
